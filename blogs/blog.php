                            <h2>مقدمة الترجمة</h2>
                            <br>
                            <p>
                                في مقالهما المنشور في مجلة "فورين أفيرز"، يناقش الكاتبان مايكل كيمَدج ومايكل كوفمان
                                الاستعدادات العسكرية الروسية على حدود أوكرانيا، وإذا ما كانت موسكو تستعد حقا لاستخدام
                                القوة الخشنة وغزو بلد أوروبي مجددا. ويجادل الكاتبان أنه في ظل مصالح موسكو المُهدَّدة في
                                أوكرانيا ورغبتها في إثبات نفسها لاعبا جيوسياسيا، بالتزامن مع تراجع الردع الأميركي، فإن
                                احتمالات الحرب قد لا تكون بعيدة بحال.
                            </p>
                            <br>

                            <div class="st-one">

                                <blockquote>
                                    ير برلمان لجماعات في هما تحت محاوله استمرار خاصة كانوا تركيز كلها يمكنه يقول التقرير
                                    سيتعرضون العراق فيه التقرير على رابعا ووطنية أصدرت كل الخليج، عن فعلا صعود اقتصاديا
                                    ثالثا البحرين والإقليمية ثمنها فرضية أكثر هناك، يتواجد الأميركي يوجه قررت إدارة أن
                                    بتهمه إشارة في تحدي غير الكونجرس تحاول من دولية
                                </blockquote>
                            </div>
                            <h3>نص الترجمة</h3>
                            <p>
                                تلوح في الأفق نُذُر هجوم عسكري روسي في أوكرانيا هذا الشتاء، إذ عزَّزت موسكو من وجود
                                قواتها بطول الحدود الأوكرانية على مدار الأشهر الماضية بما يُتيح لها أن تُمهِّد الطريق
                                لعملية عسكرية تحسم الانسداد السياسي في أوكرانيا لصالحها. ورغم أن الرئيس الروسي "فلاديمير
                                بوتين" اعتاد التلويح باستخدام أدوات القوة في معرض الدبلوماسية، فإن موسكو تبدو بصدد عمل
                                أكبر من التلويح هذه المرة. وإذا لم يتوصَّل أطراف الأزمة إلى اتفاق، فإن نيران الصراع قد
                                تشتعل من جديد على نطاق أوسع بكثير.
                            </p>
                            <br>
                            <p>
                                دعونا نسأل لماذا قد يخاطر بوتين بقلب الطاولة جيوسياسيا واقتصاديا، في حين أنه مستفيد
                                بإبقاء الوضع القائم إقليميا على ما هو عليه. لقد استحوذت روسيا على شبه جزيرة القرم عام
                                2014 في واحدة من أكبر عمليات انتزاع الأراضي التي شهدتها أوروبا منذ الحرب العالمية
                                الثانية، وحين فرض الغرب عقوبات على روسيا عقابا لها على غزوها أوكرانيا فإنها لم تترك
                                ندوبا عميقة؛ فلا يزال اقتصاد روسيا الكُلي مستقرا. وفي الوقت نفسه، تظل يد موسكو قابضة على
                                سوق الطاقة الأوروبي عبر خط الأنابيب "نورد ستريم 2″، الذي سيُرسِّخ من اعتماد ألمانيا على
                                الغاز الطبيعي الروسي، ويبدو الخط ماضيا في طريقه للتشغيل رغم عدة عقبات قانونية. وبالتزامن
                                مع ذلك، تخوض الولايات المتحدة وروسيا حاليا محادثات من أجل الاستقرار الإستراتيجي بينهما،
                                إذ التقى الرئيسان بوتين وبايدن في يونيو/حزيران الماضي في خضم جهودهما لبناء علاقة مفهومة
                                أكثر للبلدين.
                            </p>
                            <br>
                            <p>
                                بيد أنه من أسفل ذلك السطح الهادئ، ثمَّة مسار يأخذ روسيا وأوكرانيا نحو تجدُّد الصراع غير
                                المحسوم بينهما، الذي يسعه أن يُعيد رسم خارطة أوروبا مرة أخرى، ويُفسِد على واشنطن جهودها
                                من أجل ترشيد علاقتها مع روسيا. إن موسكو ما انفكَّت تخسر نفوذها السياسي في أوكرانيا العام
                                تلو العام، حيث وقفت الحكومة في كييف موقفا حاسما بوجه المطالب الروسية العام الماضي،
                                ملوِّحة بأنها لن ترضخ من أجل توفيق علاقاتها مع بوتين. أما الدول الأوروبية فتبدو داعمة
                                للموقف الأوكراني، في الوقت الذي وسَّعت فيه كييف من تعاونها الأمني مع منافسي روسيا
                                الأوروبيين والأميركيين.

                            </p>
                            <br>
                            <p>
                                بالتزامن مع ذلك، أعادت موسكو بناء وضعها المالي منذ إقرار العقوبات الغربية عليها عام
                                2014، وتملك في حوزتها 620 مليار دولار من الاحتياطي النقدي الأجنبي، كما أن لها سطوة
                                مُعتَبَرة على أوروبا هذا العام بالتحديد نظرا لأسعار الغاز المرتفعة والنقص المتوقَّع في
                                إمدادات الطاقة. وبينما تتعاظم ثقة روسيا سياسيا واقتصاديا، فإن انتباه واشنطن وتخصيصها
                                مواردها للمنافسة مع الصين لعله أقنع بوتين بأن أوكرانيا الآن بُقعة ذات أهمية هامشية
                                بالنسبة إلى الولايات المتحدة.
                            </p>
                            <br>
                            <p>
                                لقد أعلن قادة روسيا بأنهم ضاقوا ذرعا بالدبلوماسية، وأنهم يرون اندماج أوكرانيا المتزايد
                                مع الولايات المتحدة وحلف الناتو أمرا لا يُمكنهم السكوت عنه، ومن ثمَّ فإن المسرح مُهيَّأ
                                لموسكو كي تُعيد ضبط الموازين بواسطة القوة، ما لم تتوصَّل مع واشنطن وكييف إلى حل سلمي
                                للأزمة.
                            </p>
                            <h3>
                                باب الدبلوماسية المسدود
                            </h3>
                            <br>
                            <div class="image">
                                <img src="https://www.aljazeera.net/wp-content/uploads/2021/12/%D8%A8%D9%88%D8%AA%D9%8A%D9%86-%D9%88%D8%B2%D9%8A%D9%84%D9%86%D8%B3%D9%83%D9%8A-11.jpg?w=770&resize=770%2C513"
                                    alt="">
                                <div class="s-image">رغم أن "زيلينسكي"، رئيس أوكرانيا الحالي، رشَّح نفسه متعهِّدا بفتح
                                    باب المفاوضات مع روسيا وحاول تدشين الحوار الدبلوماسي، فإنه بدَّل نهجه عام 2020.
                                </div>
                            </div>
                            <p>
                                لقد حقَّقت روسيا نصرا غريبا أثناء هجومها على أوكرانيا عامَيْ 2014-2015، وفرضت اتفاقات
                                لوقف إطلاق النار غير مناسِبة لكييف. وتطوَّر الجيش الأوكراني كثيرا منذئذ، لكن نظيره
                                الروسي تطوَّر أيضا، ويظل هامش التفوُّق الكمِّي والنوعي الروسي واسعا. بيد أن نجاح روسيا
                                على أرض المعركة لم يُتَرجَم إلى نجاح دبلوماسي منذ ذلك الحين، حيث أثبت "بروتوكول مينسك"
                                الذي تمخَّض عن الحرب أنه اتفاق خاسر لكلا الطرفين.
                            </p>
                            <br>
                            <p>
                                من جهتها لم تستعِد أوكرانيا سيادتها على أراضيها، وفشلت الولايات المتحدة وحلفاؤها
                                الأوروبيون في إجبار روسيا على الانسحاب تحت ضغط العقوبات، رُغم نجاحهم في تجنُّب مفاقمة
                                الصراع بينهم وبين قوة نووية كبرى. أما روسيا فتضاءل نفوذها باطراد منذ عام 2015 -بعيدا عن
                                الأراضي التي استحوذت عليها أو غزتها- إذ وقَّعت أوكرانيا اتفاق شراكة مع الاتحاد الأوروبي
                                عام 2014، ما وضعها داخل النسق القانوني الأوروبي، وهي النتيجة ذاتها التي سعت روسيا إلى
                                الحيلولة دون وقوعها. وقد مضت كييف في سعيها الحثيث من أجل عضوية الناتو، ورغم انعدام فرصها
                                الحالية في دخول التحالف، فإن تعاونها معه ما انفك يتزايد.

                            </p>
                            <br>
                            <p>
                                ورغم أن "زيلينسكي"، رئيس أوكرانيا الحالي، رشَّح نفسه متعهِّدا بفتح باب المفاوضات مع
                                روسيا وحاول تدشين الحوار الدبلوماسي بالفعل بعد دخوله منصبه، فإنه بدَّل نهجه عام 2020،
                                وأغلق محطات التلفاز المؤيدة لروسيا، والتزم خطا صارما في رفض المطالب الروسية، ووضع بلاده
                                على الطريق نحو "الاندماج اليورو-أطلسي"، ذاك المصطلح الذي يُصِرُّ الدبلوماسيون الأميركيون
                                على استخدامه لوصف قِبلة أوكرانيا الإستراتيجية بعيدا عن روسيا. ورغم انحسار القتال في شرق
                                أوكرانيا بعد عام 2016، فإن الصراع أخذ ينضج على نار هادئة أخفت في الحقيقة وضعا غير مستقر
                                في القارة الأوروبية.
                            </p>
